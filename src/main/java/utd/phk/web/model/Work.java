package utd.phk.web.model;

public class Work {

	private String workId;
	private String workDescription;
	private String createdBy;
	private String assignStatus;
	private String assignTo;
	private String tsCreated;
	private String tsAlloted;
	private String tsExpiry;
	private String tsCompleted;
	private String compStatus;
	private String startLocation;
	private String endLocation;
	private String cost;
	
	
	public String getWorkId() {
		return workId;
	}
	public void setWorkId(String workId) {
		this.workId = workId;
	}
	public String getWorkDescription() {
		return workDescription;
	}
	public void setWorkDescription(String workDescription) {
		this.workDescription = workDescription;
	}
	public String getCreatedBy() {
		return createdBy;
	}
	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}
	public String getAssignStatus() {
		return assignStatus;
	}
	public void setAssignStatus(String assignStatus) {
		this.assignStatus = assignStatus;
	}
	public String getAssignTo() {
		return assignTo;
	}
	public void setAssignTo(String assignTo) {
		this.assignTo = assignTo;
	}
	public String getTsCreated() {
		return tsCreated;
	}
	public void setTsCreated(String tsCreated) {
		this.tsCreated = tsCreated;
	}
	public String getTsAlloted() {
		return tsAlloted;
	}
	public void setTsAlloted(String tsAlloted) {
		this.tsAlloted = tsAlloted;
	}
	public String getTsExpiry() {
		return tsExpiry;
	}
	public void setTsExpiry(String tsExpiry) {
		this.tsExpiry = tsExpiry;
	}
	public String getTsCompleted() {
		return tsCompleted;
	}
	public void setTsCompleted(String tsCompleted) {
		this.tsCompleted = tsCompleted;
	}
	public String getCompStatus() {
		return compStatus;
	}
	public void setCompStatus(String compStatus) {
		this.compStatus = compStatus;
	}
	public String getStartLocation() {
		return startLocation;
	}
	public void setStartLocation(String startLocation) {
		this.startLocation = startLocation;
	}
	public String getEndLocation() {
		return endLocation;
	}
	public void setEndLocation(String endLocation) {
		this.endLocation = endLocation;
	}
	public String getCost() {
		return cost;
	}
	public void setCost(String cost) {
		this.cost = cost;
	}
	

}
